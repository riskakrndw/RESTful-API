package calculator

func Addition(angkaPertama, angkaKedua int) int {
	return angkaPertama + angkaKedua
}

func Subtraction(angkaPertama, angkaKedua int) int {
	return angkaPertama - angkaKedua
}

func Division(angkaPertama, angkaKedua int) float64 {
	return float64(angkaPertama) / float64(angkaKedua)
}

func Multiplication(angkaPertama, angkaKedua int) int {
	return angkaPertama * angkaKedua
}
